package edu.agile.Models;

public class Difficulty {
    private int width;
    private int height;
    private long bombAmount;

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public long getBombAmount() {
        return bombAmount;
    }

    public void setBombAmount(long bombAmount) {
        this.bombAmount = bombAmount;
    }
}
